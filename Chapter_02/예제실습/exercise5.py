# 리스트 자료형
# 리스트명 = [요소1. 요소2, 요소3, ...]

odd = [1,3,5,7,9]
print(odd)

# 여러가지 리스트
a = []
b = [1,2,3]
c = ['Life', 'is', 'too', 'short']
d = [1,2, 'Life', 'is']
e = [1,2, ['Life', 'is']]

print(a)
print(b)
print(c)
print(d)
print(e)

# 리스트의 인덱싱과 슬라이싱
# 리스트도 문자열처럼 인덱싱과 슬라이싱이 가능하다

# 리스트 인덱싱
a = [1,2,3]
print(a)

# a[0]은 리스트 a의 첫번째 요소 값을 말한다
print(a[0])
print(a[0] + a[2]) # 1+3
